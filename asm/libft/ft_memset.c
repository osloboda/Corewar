#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*bb;

	i = 0;
	bb = (char*)b;
	while (i < len)
	{
		bb[i] = c;
		i++;
	}
	return (bb);
}
